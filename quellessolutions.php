
            <h3>Quels sont les moyens d’enrayer le braconnage ? </h3>
<h5>L’information de terrain est primordiale pour tenter de démanteler les filières et certains pays comme le Kenya se sont carrément inspirés des méthodes de la lutte antiterroriste pour protéger les grands mammifères menacés. </h5>

<h5>Les initiatives sont basées sur le recueil d’information auprès des communautés locales pour identifier les suspects, leur localisation et leur rôle supposé dans le réseau.</h5>

<h5>La lutte passe aussi par le renforcement des lois anti-braconnage, par la formation de rangers locaux, des gardes armés.</h5>

<h5>Celle-ci est aussi technologique :</h5>
<ul>
    <li> pose de capteurs directement sur les animaux afin de suivre leurs déplacements.</li>
    <li>Drones à caméras thermiques et intelligence artificielle.</li>
    <li>Injection d’isotopes radioactifs dans les cornes des rhinocéros.</li>
    <li>Recherche d’ADN d’animaux protégés sur les armes et les vêtements des suspects.</li>
</ul>
<h5>Le soutient des populations locales est essentiel : accès à l’éducation et aux soins, développement économique.</h5> 
<h5>Les campagnes de sensibilisation sont aussi indispensables pour inciter les consommateurs à changer leurs comportements. </h5> 
<h5>Et tous ces efforts mis bout à bout semblent porter leur fruit. L’augmentation des patrouilles, l’amélioration des moyens de surveillances couplés avec la coopération internationale a fait ses preuves par endroit. Ainsi, l’Afrique du Sud qui abrite 80% de la population mondiale de rhinocéros, a réussi à réduire de 23% le nombre de ses animaux tués en 2019. Selon les statistiques officielles dévoilées en février 594 rhinocéros ont été abattus l’année dernière. On est certes loin du quota zéro, mais la tendance est à la baisse.</h5>

<h3>Le trafic d'espèces menacées en chiffres (effrayants)</h5>

<p><strong>800</strong> espèces. C'est le nombre d'espèces inscrites à l'Annexe I de la Convention sur le commerce international des espèces de faune et de flore sauvages menacées d'extinction (Cites), signée en 1973 et ratifiée par 175 pays. </p>

<p><strong>2 000</strong> cornes. C'est le nombre de nouvelles cornes de rhinocéros entrées dans le trafic en provenance d'Afrique l'an dernier, soit <strong>30</strong> fois plus qu'en 2000</p>

<p>Aujourd'hui, un éléphant meurt toutes les 15 minutes dans le monde pour son ivoire.</p>

<p><strong>51 000</strong> euros/kilo. C'est le prix de la corne de rhinocéros sur le marché noir, bien plus élevé que celui de l’or (<strong>37 000 euros</strong>) ou du platine (<strong>43 200</strong>). </p>

<p><strong>1 000</strong> rangers. C'est le nombre d'éco-gardes qui ont été tués par des braconniers ces dix dernières années, dans 35 pays différents, lors d'incidents liés au trafic d'espèces sauvages, selon un décompte de l'ONG Game Rangers International. </p>

<p><strong>9 500</strong> annonces. Le commerce d’animaux sauvages se fait de plus en plus en ligne, ce qui complique la lutte contre cette criminalité, comme l'a démontré le dernier rapport de l'IFAW, Recherché, mort ou vif, publié fin novembre. </p>

<p> En France:un particulier coupable de trafic d'espèce sauvage risque jusqu'à <strong>150 000</strong> euros d'amende et 3 ans de prison, et une bande organisée, jusqu'à <strong>750 000 euros et 7 ans de prison.</strong> </p>