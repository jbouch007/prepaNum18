
            
            <h3>Déforestation</h3>
            <li>L’expansion agricole en zone tropicale est la première cause de la déforestation.</li>
            
            <p>Plus de la moitié de la déforestation mondiale est directement liée à la conversion des forêts en zones de culture ou de pâturage</p>
            
            <p>Les fronts de la déforestation se multiplient et s’étendent, alerte le Fonds mondial pour la nature, le WWF. La Terre, qui était couverte à 50 % de forêts il y a huit mille ans, ne l’est plus qu’à 30 %. Non seulement de nouvelles zones soumises aux incendies et aux défrichements apparaissent en Afrique – au Liberia, au Ghana, à Madagascar – et en Amérique latine – notamment au Mexique et au Guatemala –, mais la destruction des jungles, forêts primaires ou sèches, savanes arborées s’accélère partout sur la planète.</p>
            
            <p>Dans un rapport rendu public mercredi 13 janvier, l’ONG recense et analyse les vingt-quatre principaux fronts dans trente pays. Cinq ans après leur précédente analyse, la situation s’est encore dégradée : à eux seuls, ces vingt-quatre fronts menacent un cinquième des forêts tropicales du monde.</p>
            
